/// ValidationError represents an error that occurs during validation of a message descriptor.
#[derive(Serialize, Deserialize, Error, Debug)]
#[error("Validation error: {message}")]
pub struct ValidationError {
    /// The error message.
    pub message: String,
}

pub trait MessageParameters {
    type Descriptor: MessageDescriptor;
    type Fields: MessageFields;

    #[allow(async_fn_in_trait)]
    async fn build(&self) -> Result<(Self::Descriptor, Option<Self::Fields>), ValidationError> {
        Err(ValidationError {
            message: String::from("not implemented"),
        })
    }

    fn delegated_grant(&self) -> Option<Message<RecordsWriteDescriptor>> {
        None
    }

    fn permission_grant_id(&self) -> Option<String> {
        None
    }

    fn protocol_rule(&self) -> Option<String> {
        None
    }
}

impl MessageParameters for () {
    type Descriptor = Descriptor;
    type Fields = Fields;
}

/// ConcreteDescriptor is implemented by every concrete message descriptor (normally via the
/// `#[descriptor]`/`#[interface]` macros). It exposes the descriptor's `interface`/`method` as
/// associated constants, which back both the typed deserialization guard and union dispatch.
pub trait ConcreteDescriptor: MessageDescriptor + Serialize + DeserializeOwned + PartialEq {
    const INTERFACE: &'static str;
    const METHOD: &'static str;
    /// The concatenated `INTERFACE`+`METHOD` handler key (e.g. `RecordsWrite`), built at compile
    /// time by the `#[descriptor]` macro. Backs [`MessageKind::as_str`](super::MessageKind::as_str)
    /// without a runtime allocation.
    const KEY: &'static str;
    /// The JSON-schema identifier validating this descriptor's messages, or `None` for descriptors
    /// with no published schema (the `no_handler`, spec-parity-only `MessagesQuery`). Set via the
    /// `#[descriptor(schema_id = …)]` attribute; backs the descriptor-derived schema lookup in
    /// `dwn::validation`.
    const SCHEMA_ID: Option<&'static str>;
}

/// FromDescriptor is implemented by every concrete message descriptor (normally via the
/// `#[interface]` macro). It downcasts a borrowed `Descriptor` union to the concrete descriptor
/// type, returning a borrowed reference (clone at the call site if an owned value is needed).
pub trait FromDescriptor: ConcreteDescriptor {
    fn from_descriptor(descriptor: &Descriptor) -> Result<&Self, ValidationError>;
}

/// Implemented for each interface union (`Records`/`Protocols`/`Messages`), generated by the
/// `#[interface]` macro. Exposes the union's interface name and the full set of descriptor kinds
/// it can deserialize as `(interface, method, has_handler)` tuples.
///
/// `has_handler` is `false` for descriptors that are deserializable (round-tripped for spec
/// parity) but have no request handler in this implementation — e.g. `MessagesQuery`. This is the
/// single source of truth for the dispatch surface: `current_handler_kinds()` filters on it.
pub trait InterfaceUnion {
    const INTERFACE: &'static str;
    const KINDS: &'static [(&'static str, &'static str, bool)];
}

/// MessageDescriptor is a trait that all message descriptors must implement.
/// It provides the interface and method for the message descriptor. The generic `Descriptor`
/// implements this trait for use when the concrete type is not known. Concrete Descriptor types
/// implement this trait directly (or use the derive macro).
pub trait MessageDescriptor: Serialize + DeserializeOwned + PartialEq {
    type Fields: MessageFields
        + Serialize
        + DeserializeOwned
        + std::fmt::Debug
        + PartialEq
        + Send
        + Sync
        + Clone;

    type Parameters: MessageParameters + Send + Sync;

    fn interface(&self) -> &'static str;
    fn method(&self) -> &'static str;

    fn cid(&self) -> cid::Cid {
        generate_cid_from_serialized(self)
            .expect("Failed to generate CID from serialized message descriptor")
    }
}

